class Complex {

    private double real;
    private double imaginary;
    public double getReal() {
        return this.real;
    }
    public double getImaginary() {
        return this.imaginary;
    }
    public void setReal( double newReal) {
        this.real = newReal;
    }
    public void setImaginary( double newImaginary) {
        this.imaginary = newImaginary;
    }
}